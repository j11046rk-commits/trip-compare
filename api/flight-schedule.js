// ===== 逆方向を自動生成（DB未登録路線の対称ルート） =====
for (const key of Object.keys(DB)) {
  const [from, to] = key.split('-');
  const rev = `${to}-${from}`;
  if (!DB[rev]) {
    DB[rev] = DB[key].map(([co, dep, arr, min, fare]) => [co, dep, arr, min, fare]);
  }
}

// ---- 時刻を分に変換 ----
function toMin(hhmm) {
  const [h, m] = hhmm.split(':').map(Number);
  return h * 60 + m;
}

// ---- メインハンドラ ----
module.exports = (req, res) => {
  const { from, to, datetime, isarr } = req.query;
  if (!from || !to || !datetime) {
    return res.status(400).json({ error: 'INVALID_REQUEST' });
  }

  const timeStr = datetime.slice(8, 12);   // HHMM
  const reqMin  = parseInt(timeStr.slice(0,2)) * 60 + parseInt(timeStr.slice(2,4));
  const isArr   = isarr === 'true';

  const key = `${from}-${to}`;
  const flights = DB[key];

  res.setHeader('Cache-Control', 'no-store');

  if (!flights || !flights.length) {
    return res.status(200).json({ status: 'NO_ROUTE', flights: [] });
  }

  // 指定時刻以降（出発指定）または以前（到着指定）の便を抽出
  const filtered = flights.filter(([, dep, arr]) => {
    const depMin = toMin(dep);
    const arrMin = toMin(arr);
    if (!isArr) return depMin >= reqMin;
    return arrMin <= reqMin;
  });

  // 便が少なすぎる場合は全便から近い順に返す
  const list = filtered.length >= 2 ? filtered : flights;

  // 最大5便を返す
  const result = list.slice(0, 5).map(([airline, dep, arr, durationMin, normalFare]) => ({
    airline,
    dep,
    arr,
    durationMin,
    normalFare,        // 普通運賃（正規）
    earlyFare: Math.round(normalFare * 0.55 / 10) * 10,  // 早割（約45%引き）
  }));

  console.log(`[flight-schedule] ${key} ${isArr ? '着' : '発'}${timeStr}: ${result.length}件`);
  res.status(200).json({ status: 'OK', flights: result, source: 'schedule_db' });
};
